#!/bin/bash

# 1. Υπολογισμός δευτερολέπτων μέχρι τις 00:00 (μεσάνυχτα)
WAIT_SECONDS=$(( $(date -d "tomorrow 00:00" +%s) - $(date +%s) ))

# 2. Το script περιμένει (κοιμάται) μέχρι τα μεσάνυχτα καταναλώνοντας μηδενική CPU
sleep $WAIT_SECONDS

# 3. Ξεκινάει το κυρίως πρόγραμμα C στο παρασκήνιο
./sysprog > terminal_output.txt 2>&1 &

# 4. Αποθηκεύουμε την ταυτότητα (PID) του sysprog
PID=$!

# 5. Περιμένει αυστηρά 24 ώρες (24 ώρες * 60 λεπτά * 60 δεύτερα = 86400)
sleep 86400

# 6. Τερματισμός του προγράμματος με ασφάλεια (Graceful exit)
kill $PID